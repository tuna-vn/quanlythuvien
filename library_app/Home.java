    private void loadsach() {
        try {

            conn = ConnectDB.KetnoiDB();
            String sql = "Select SUM(SoLuong) from sach";
            PreparedStatement ps = conn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                int tongSoLuong = rs.getInt(1); // Lấy tổng số lượng từ kết quả truy vấn
                sl.setText(String.valueOf(tongSoLuong)); // Đưa giá trị vào text field
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }
