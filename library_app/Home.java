    private void loadS() {
        sachTable.removeAll();
        try {
            conn = ConnectDB.KetnoiDB();
            String sql = "SELECT sach.MaSach,sach.TenSach,tacgia.TenTacGia,theloai.TenTheLoai,nxb.TenNXB From sach join tacgia on sach.MaTacGia=tacgia.MaTacGia join theloai on sach.MaTheLoai=theloai.MaTheLoai JOIN nxb on sach.MaNXB=nxb.MaNXB;";
            Statement s = conn.createStatement();
            ResultSet rs = s.executeQuery(sql);

            String[] arr = {"Mã sách", "Tên sách", "Tên tác giả", "Thể loại", " NXB"};
            DefaultTableModel model = new DefaultTableModel(arr, 0);
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString(1).toString());
                v.add(rs.getString(2).toString());
                v.add(rs.getString(3).toString());
                v.add(rs.getString(4).toString());
                v.add(rs.getString(5).toString());

                model.addRow(v);
            }
            sachTable.setModel(model);
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

