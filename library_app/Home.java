    private void loadtk() {
        try {
            conn = ConnectDB.KetnoiDB();
            String sql = "SELECT (SELECT SUM(SoLuong) FROM sach) - (SELECT SUM(SoLuong) FROM chitietmuon) AS HieuSoLuong";
            PreparedStatement ps = conn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                int hieuSoLuong = rs.getInt("HieuSoLuong");
                tonkho.setText(String.valueOf(hieuSoLuong));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

